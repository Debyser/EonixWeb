CREATE TABLE country (
	id INT IDENTITY(1,1),
	iso_3_code varchar(3) NOT NULL,
	name varchar(50) NOT NULL,
		active bit DEFAULT 1 NOT NULL,
	CONSTRAINT country_pk PRIMARY KEY (id)
);

